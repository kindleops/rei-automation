export function buildEnrichmentCoverageDiagnostics(row = {}) {
  return {
    property_resolved: Boolean(clean(row.property_id) && clean(row.property_address_full)),
    owner_resolved: Boolean(clean(row.master_owner_id) && clean(row.owner_name || row.seller_display_name)),
    market_resolved: Boolean(clean(row.market)),
    valuation_resolved: Number(row.estimated_value) > 0 || Number(row.equity_amount) > 0 || Number(row.equity_percent) > 0,
    classification_resolved: Boolean(clean(row.inbox_bucket) || clean(row.detected_intent)),
    message_history_resolved: Boolean(clean(row.latest_message_body) && clean(row.latest_message_at)),
    contact_identity_resolved: clean(row.contact_identity_class) && row.contact_identity_class !== "unknown",
  };
}