/**
 * Interpolate {{key}} placeholders in a title template.
 *
 * @param {string} template
 * @param {Record<string, string|number|null|undefined>} vars
 * @returns {string}
 */
export function renderTitleTemplate(template, vars = {}) {
  return String(template ?? '').replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const value = vars[key]
    return value != null && String(value).trim() !== '' ? String(value) : key
  })
}

/**
 * Resolve catalog entry for an event type.
 *
 * @param {string} eventType
 * @returns {object|null}
 */
export function resolveEventCatalogEntry(eventType) {
  return EVENT_CATALOG[String(eventType ?? '').trim()] ?? null
}

