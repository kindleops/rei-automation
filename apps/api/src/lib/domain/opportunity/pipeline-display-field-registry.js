export function getRegistryField(key) {
  return PIPELINE_DISPLAY_FIELD_REGISTRY[key] ?? null
}

export function getSortableFields() {
  return Object.values(PIPELINE_DISPLAY_FIELD_REGISTRY).filter((f) => f.sortable)
}

export function getFilterableFields() {
  return Object.values(PIPELINE_DISPLAY_FIELD_REGISTRY).filter((f) => f.filterable)
}

export function isOfferPlusStage(stageCode) {
  const code = String(stageCode ?? '').trim().toLowerCase()
  return OFFER_STAGES.has(code)
    || code.includes('offer')
    || code.includes('contract')
    || code.includes('closing')
    || code.includes('disposition');
}

export function exportRegistryForClient() {
  return Object.values(PIPELINE_DISPLAY_FIELD_REGISTRY).map((f) => ({
    key: f.key,
    label: f.label,
    description: f.description,
    group: inferGroup(f.key),
    dataType: f.dataType,
    emptyLabel: f.emptyLabel,
    sortable: f.sortable,
    filterable: f.filterable,
    groupable: f.groupable,
    cardCompatible: f.cardCompatible,
    detailPanelCompatible: f.detailPanelCompatible,
    stageApplicability: f.stageApplicability,
    visibilityCondition: f.visibilityCondition ?? null,
    editable: false,
    calculated: f.sourceEntity === 'computed' || f.key === 'stage_age' || f.key === 'reply_attention_state',
    canBeStale: ['aos', 'aos_confidence', 'arv', 'recommended_offer', 'strategy'].includes(f.key),
    operators: operatorsForType(f.dataType),
  }));
}

function inferGroup(key) {
  if (['seller_display_name', 'portfolio_property_count'].includes(key)) return 'seller_owner'
  if (['property_address_full', 'market', 'property_type', 'property_state', 'units_count', 'estimated_value', 'equity_amount', 'property_type_market'].includes(key)) return 'property'
  if (['latest_message_preview', 'latest_intent', 'conversation_state', 'reply_attention_state', 'last_activity_at', 'last_contact_at'].includes(key)) return 'conversation'
  if (['pipeline_stage', 'universal_status', 'temperature', 'stage_age', 'stage_entered_at', 'priority', 'next_action', 'next_action_due', 'follow_up_due', 'follow_up_reason', 'blocker', 'automation_state', 'workflow_state', 'queue_state', 'opportunity_status'].includes(key)) return 'pipeline'
  return 'deal_intelligence'
}

function operatorsForType(dataType) {
  if (dataType === 'number' || dataType === 'currency' || dataType === 'score' || dataType === 'percent') {
    return ['gt', 'gte', 'lt', 'lte', 'between', 'is_known', 'is_unknown']
  }
  if (dataType === 'datetime' || dataType === 'date') {
    return ['before', 'after', 'between', 'today', 'overdue', 'within_next_days', 'within_last_days', 'is_known', 'is_unknown']
  }
  if (dataType === 'select') {
    return ['is', 'is_not', 'is_any_of', 'is_none_of']
  }
  if (dataType === 'boolean') {
    return ['yes', 'no', 'unknown']
  }
  return ['equals', 'contains', 'starts_with', 'is_known', 'is_unknown']
}